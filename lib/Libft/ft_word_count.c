#include "libft.h"

size_t	ft_word_count(char const *s, char c)
{
	size_t	i;

	i = 0;
	while(*s)
	{
		if (*s == c)
			s++;
		else
		{
			if (*s =='\n')
				break;
			i++;
			while (*s && *s != c)
				s++;
		}
	}
	return (i);
}
